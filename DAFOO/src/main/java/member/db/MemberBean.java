package member.db;


public class MemberBean {
	
	
	private String id;
	private String nick;
	private String pass;
	private String name;
	private int age;
	private String gender;
	private int height;
	private int weight;
	private String phone;
	private String email;
	private String postcode;
	private String address;
	private String detailAddress;
	private String extraAddress;

	
	

	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getNick() {
		return nick;
	}
	public void setNick(String nick) {
		this.nick = nick;
	}
	public String getPass() {
		return pass;
	}
	public void setPass(String pass) {
		this.pass = pass;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	public int getWeight() {
		return weight;
	}
	public void setWeight(int weight) {
		this.weight = weight;
	}
	
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPostcode() {
		return postcode;
	}
	public void setPostcode(String postcode) {
		this.postcode = postcode;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getDetailAddress() {
		return detailAddress;
	}
	public void setDetailAddress(String detailAddress) {
		this.detailAddress = detailAddress;
	}
	public String getExtraAddress() {
		return extraAddress;
	}
	public void setExtraAddress(String extraAddress) {
		this.extraAddress = extraAddress;
	}
	
	@Override
	public String toString() {
		return "MemberBean [id=" + id + ", nick="+ nick + " , pass=" + pass + ", name=" + name + ", gender="+ gender
				+", age=" + age + ", height=" + height+ ", weight=" + weight + ", phone=" + phone + ", email=" + email + ", postcode=" + postcode
				+ ", address=" + address + ", detailAddress=" + detailAddress + ", extraAddress=" + extraAddress + "]";
	}
	
	
}
